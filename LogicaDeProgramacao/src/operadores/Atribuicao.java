package operadores;

public class Atribuicao {
   
	public static void main(String[] args) {
		
		// operadores de atribuição: = += -= *= /= %=
		
		double bonus = 1800;
		bonus += 1000; // 2800
		bonus -= 1000; // 1800
		bonus *= 2;   // 3600
		bonus /= 4;  // 900
		
		System.out.println(bonus);
		
		
		
		
	}
	
}
